let web3;
let contract;
let accounts;

const initWeb3 = () => {
  // Check if MetaMask is installed
  if (window.ethereum) {
    web3 = new Web3(window.ethereum);
    window.ethereum.request({ method: "eth_requestAccounts" })
      .then((accs) => {
        accounts = accs;
      })
      .catch((err) => console.log("Error fetching accounts:", err));
  } else {
    alert("Please install MetaMask!");
  }
};

const getContract = () => {
  if (!web3) {
    initWeb3(); // Initialize web3 if not done already
    return null; // Return null until web3 is initialized
  }
  
  if (!contract) {
    contract = new web3.eth.Contract(VOTING_ABI, CONTRACT_ADDRESS);
  }
  return contract;
};

